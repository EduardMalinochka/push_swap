#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		len;
	char	*res;
	int		i;

	len = ft_strlen(s1);
	res = malloc(len + 1);
	if (!res)
		return (0);
	i = 0;
	while (s1[i] != '\0')
	{
		res[i] = s1[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
