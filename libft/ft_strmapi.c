#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*res;
	int				len;
	unsigned int	i;

	if (!s)
		return (0);
	len = ft_strlen(s);
	res = malloc(len + 1);
	if (!res)
		return (0);
	i = 0;
	while (s[i] != '\0')
	{
		res[i] = f(i, s[i]);
		i++;
	}
	res[i] = '\0';
	return (res);
}
